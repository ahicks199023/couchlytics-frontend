export default function EmojiPicker({ onEmojiSelect, onClose, className = '' }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = useState<keyof typeof EMOJI_CATEGORIES>('Smileys & People')

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji)
    onClose()
  }

  return (
    <div className={`bg-gray-800 border border-gray-700 rounded-lg shadow-lg p-4 max-w-sm ${className}`}>
      {/* Category Tabs */}
      <div className="flex flex-wrap gap-1 mb-3 border-b border-gray-700 pb-2">
        {Object.keys(EMOJI_CATEGORIES).map((category) => (
          <button
            key={category}
            onClick={() => setSelectedCategory(category as keyof typeof EMOJI_CATEGORIES)}
            className={`px-2 py-1 text-xs rounded transition-colors ${
              selectedCategory === category
                ? 'bg-blue-600 text-white'
                : 'text-gray-400 hover:text-white hover:bg-gray-700'
            }`}
          >
            {category.split(' ')[0]}
          </button>
        ))}
      </div>

      {/* Emoji Grid */}
      <div className="grid grid-cols-8 gap-1 max-h-48 overflow-y-auto">
        {EMOJI_CATEGORIES[selectedCategory].map((emoji, index) => (
          <button
            key={`${selectedCategory}-${index}`}
            onClick={() => handleEmojiClick(emoji)}
            className="w-8 h-8 flex items-center justify-center text-lg hover:bg-gray-700 rounded transition-colors"
            title={emoji}
          >
            {emoji}
          </button>
        ))}
      </div>

      {/* Close Button */}
      <div className="flex justify-end mt-3 pt-2 border-t border-gray-700">
        <button
          onClick={onClose}
          className="px-3 py-1 text-sm text-gray-400 hover:text-white transition-colors"
        >
          Close
        </button>
      </div>
    </div>
  )
}
